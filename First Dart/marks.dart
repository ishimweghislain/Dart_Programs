void main() {
  int score = 90;

  if (score >= 90) {
    print("Your Grade is A");
  } else if (score >= 80 && score < 90) {
    print("Your Grade is B");
  } else if (score >= 70 && score < 80) {
    print("Your Grade is C");
  } else if (score >= 60 && score < 70) {
    print("Your Grade is D");
  } else {
    print("Your Grade is F");
  }
  
}
